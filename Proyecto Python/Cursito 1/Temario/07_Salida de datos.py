nombre = "Sebastian"
edad = 20

# Usando la concatenación simple con comas
print("Hola", nombre, "tienes", edad, "años de edad")

# Usando el método format()
print("Hola {} tienes {} años de edad".format(nombre, edad))

# Usando f-strings (requiere Python 3.6+)
print(f"Hola {nombre} tienes {edad} años de edad")
