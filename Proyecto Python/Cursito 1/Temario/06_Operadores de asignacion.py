a = 20
b = 23
c = 25

a += 5  # Equivalente a: a = a + 5
print(a)  # Asignación con suma (+=):

b -= 3  # Equivalente a: b = b - 3
print(b)  # Asignación con resta (-=)

c *= 2  # Equivalente a: c = c * 2
print(c)  # Asignación con multiplicación (*=):

a /= 2  # Equivalente a: a = a / 2
print(a)  # Asignación con división (/=):

b //= 2  # Equivalente a: b = b // 2
print(b)  # Asignación con división entera (//=):

c %= 6  # Equivalente a: c = c % 6
print(c)  # Asignación con módulo (%=)

a **= 2  # Equivalente a: a = a ** 2
print(a)  # Asignación con potenciación (**=)

a = 20
a &= 15  # Equivalente a: a = a & 15
print(a)  # Asignación con bit a bit AND (&=):

b = 23
b |= 10  # Equivalente a: b = b | 10
print(b)  # Asignación con bit a bit OR (|=):

c = 25
c ^= 14  # Equivalente a: c = c ^ 14
print(c)  # Asignación con bit a bit XOR (^=):

a = 20
a >>= 2  # Equivalente a: a = a >> 2
print(a)  # Asignación con desplazamiento a la derecha (>>=):

b = 23
b <<= 1  # Equivalente a: b = b << 1
print(b)  # Asignación con desplazamiento a la izquierda (<<=):
